public class NumberPositiveorNegative {
    public static void main(String[] args) {

        double number = 50.0;

        if (number > 0.0) {
            System.out.println(number + " is positive number");
        } else if (number < 0.0) {
            System.out.println(number + "is negative number");
        } else {
            System.out.println(number + " is Zero");
        }
    }
}
